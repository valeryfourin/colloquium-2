'''
Сформуйте лінійний масив дійсних чисел, елементи якого є відстанями,
пройденими тілом при вільному падінні на землю за 1, 2, ..., 10 с.
Bиконала Новіцька В. І. 122Б
'''
for t in range(1,11):
    h = (t**2 * 9.80665)/2 # формула відстані виражена з формули прискореного падіння
    print(f'Distance on the {t} second: {h}') # довжина відстані кожної секунди